    <h1>En quelques mots…</h1>
    <p>
        La Menuiserie Collaborative est un projet d’atelier partagé autour du travail du bois, à Montpellier.<br>
        <br>
        Cet espace permettra aux professionnels du bois, artisans menuisiers, ébénistes, designers, luthiers, charpentiers… d’avoir accès à des espaces équipés de machines à bois conventionnelles et numériques.<br>
        <br>
        L’atelier accueillera aussi différents publics: amateurs curieux, bricoleurs débutants ou confirmés.<br>
        <br>
        La Menuiserie Collaborative souhaite offrir à chacun l’opportunité de concevoir et créer dans des conditions professionnelles et sécurisées, tout en bénéficiant d’accompagnements et de conseils d’experts.
    </p>
    <br>
